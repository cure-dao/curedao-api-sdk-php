	public static function getIconForString(string $str, $id): ?string{
		if($id && stripos($str, 'variable category') !== false){
			$cat = BaseVariableCategory::find($id);
			return $cat->getFontAwesome();
		}
		if(stripos($str, 'cause') !== false){
			return self::SIGN_IN_ALT_SOLID;
		}
		if(stripos($str, 'effect') !== false){
			return self::SIGN_OUT_ALT_SOLID;
		}
		return null;
	}
	public static function html(string $icon): string{
		return "<i class=\"$icon\"></i>";
	}
	public static function getAdminIcon(): string{
		return static::html(static::ADMIN);
	}
	public static function findIconLike(string $search, string $default = null): ?string{
		if(isset(self::$searchResults[$search])){
			if(self::$searchResults[$search] === false){
				return $default;
			}
			return self::$searchResults[$search];
		}
		$icon = self::findConstantValueWithNameLike($search, $default);
		if(!$default && !$icon){
			self::$searchResults[$search] = false;
			return $default;
		}
		return self::$searchResults[$search] = $icon;
	}
